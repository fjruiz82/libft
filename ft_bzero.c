#include <stdio.h>

void	ft_bzero(void *str, size_t n)
{
	unsigned char	*s1;

	s1 = str;
	while (n > 0 && *s1 != '\0')
	{
		if (*s1 != '\0')
		{
			*s1 = '\0';
			s1++;
			n--;
		}
	}
}
